package academy.learnprogramming;

public class UnaryOperator {

    public static void main(String[] args) {
        int x = + 3; // + sign is redundant

        double y = -x; // - unary operator

        y = -y; // y * -1

        boolean a  = true;
        boolean b = !a; // would be false

        b = !b; // would be true


    }
}
